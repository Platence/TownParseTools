package townInfoPackega;

public class Person {

    private String name;
    private String defoltcity;

    public Person(String name, String defoltcity) {
        this.name = name;
        this.defoltcity = defoltcity;
    }

    public String getName() {
        return name;
    }

    public String getDefoltcity() {
        return defoltcity;
    }
}
